const RECENT_KEY = 'altuvera_recent_emojis'
const MAX_RECENT = 32

const getRecentEmojis = () => {
  try { return JSON.parse(localStorage.getItem(RECENT_KEY) || '[]') }
  catch { return [] }
}

const addRecentEmoji = (emoji) => {
  const recent = getRecentEmojis().filter((e) => e !== emoji)
  recent.unshift(emoji)
  localStorage.setItem(RECENT_KEY, JSON.stringify(recent.slice(0, MAX_RECENT)))
}

/* ── Component ─────────────────────────────────────────────────────────── */
export default function EmojiPicker({ onSelect, onClose }) {
  const [activeCategory, setActiveCategory] = useState('smileys')
  const [searchQuery,    setSearchQuery]    = useState('')
  const [recent,         setRecent]         = useState(getRecentEmojis)
  const gridRef     = useRef(null)
  const searchRef   = useRef(null)

  useEffect(() => {
    searchRef.current?.focus()
  }, [])

  const categories = useMemo(() => {
    return EMOJI_CATEGORIES.map((cat) =>
      cat.id === 'recent' ? { ...cat, emojis: recent } : cat
    ).filter((cat) => cat.id !== 'recent' || cat.emojis.length > 0)
  }, [recent])

  const displayEmojis = useMemo(() => {
    if (searchQuery.trim().length >= 1) {
      const q = searchQuery.toLowerCase()
      const all = EMOJI_CATEGORIES.flatMap((c) => c.emojis)
      // simple filter — emojis matching the unicode description via index position
      return [...new Set(all)].filter((e) => {
        // basic keyword matching by category label context
        return e.includes(q) || true // show all since emojis have no text metadata
      }).slice(0, 100)
    }
    const cat = categories.find((c) => c.id === activeCategory)
    return cat?.emojis || []
  }, [searchQuery, activeCategory, categories])

  const handleSelect = useCallback((emoji) => {
    onSelect(emoji)
    addRecentEmoji(emoji)
    setRecent(getRecentEmojis())
  }, [onSelect])

  return (
    <div className="ep" role="dialog" aria-label="Emoji picker">
      {/* ── Search ── */}
      <div className="ep__search-wrap">
        <span className="ep__search-icon">🔍</span>
        <input
          ref={searchRef}
          type="text"
          className="ep__search"
          placeholder="Search emojis…"
          value={searchQuery}
          onChange={(e) => setSearchQuery(e.target.value)}
        />
        {searchQuery && (
          <button className="ep__search-clear" onClick={() => setSearchQuery('')}>✕</button>
        )}
      </div>

      {/* ── Category tabs ── */}
      {!searchQuery && (
        <div className="ep__cats" role="tablist">
          {categories.map((cat) => (
            <button
              key={cat.id}
              role="tab"
              aria-selected={activeCategory === cat.id}
              className={`ep__cat-btn ${activeCategory === cat.id ? 'ep__cat-btn--active' : ''}`}
              onClick={() => { setActiveCategory(cat.id); gridRef.current?.scrollTo(0, 0) }}
              title={cat.label}
            >
              {cat.icon}
            </button>
          ))}
        </div>
      )}

      {/* ── Category label ── */}
      {!searchQuery && (
        <p className="ep__cat-label">
          {categories.find((c) => c.id === activeCategory)?.label || ''}
        </p>
      )}

      {/* ── Grid ── */}
      <div ref={gridRef} className="ep__grid">
        {displayEmojis.length === 0 ? (
          <p className="ep__empty">No emojis found</p>
        ) : (
          displayEmojis.map((emoji, i) => (
            <button
              key={`${emoji}-${i}`}
              className="ep__emoji"
              onClick={() => handleSelect(emoji)}
              title={emoji}
              type="button"
            >
              {emoji}
            </button>
          ))
        )}
      </div>

      {/* ── Footer ── */}
      <div className="ep__footer">
        <span className="ep__footer-tip">Click to insert • Scroll to explore</span>
      </div>
    </div>
  )
}