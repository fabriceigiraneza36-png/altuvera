/**
 * Add professional header to PDF
 */
const addPDFHeader = (pdf, title, subtitle = '') => {
  const pageWidth = pdf.internal.pageSize.getWidth();

  // Background accent bar
  pdf.setFillColor(...COLORS.primary);
  pdf.rect(0, 0, pageWidth, 35, 'F');

  // Logo/Brand
  pdf.setFontSize(28);
  pdf.setFont('helvetica', 'bold');
  pdf.setTextColor(...COLORS.white);
  pdf.text('ALTUVERA', 20, 18);

  pdf.setFontSize(10);
  pdf.setFont('helvetica', 'normal');
  pdf.setTextColor(...COLORS.primaryLight);
  pdf.text('East African Safari & Tours', 20, 26);

  // Title section
  if (title) {
    pdf.setFontSize(18);
    pdf.setFont('helvetica', 'bold');
    pdf.setTextColor(...COLORS.primary);
    pdf.text(title, 20, 50);
  }

  if (subtitle) {
    pdf.setFontSize(11);
    pdf.setFont('helvetica', 'normal');
    pdf.setTextColor(...COLORS.textLight);
    pdf.text(subtitle, 20, 58);
  }

  return 65; // Return starting Y position for content
};

/**
 * Add professional footer to PDF
 */
const addPDFFooter = (pdf, pageNumber = 1, totalPages = 1) => {
  const pageHeight = pdf.internal.pageSize.getHeight();
  const pageWidth = pdf.internal.pageSize.getWidth();

  // Footer line
  pdf.setDrawColor(...COLORS.primary);
  pdf.setLineWidth(0.5);
  pdf.line(20, pageHeight - 20, pageWidth - 20, pageHeight - 20);

  // Footer text
  pdf.setFontSize(9);
  pdf.setFont('helvetica', 'normal');
  pdf.setTextColor(...COLORS.textLight);
  
  const leftText = `Generated by Altuvera | ${new Date().toLocaleDateString('en-US', { 
    year: 'numeric', 
    month: 'long', 
    day: 'numeric' 
  })}`;
  
  const rightText = `Page ${pageNumber} of ${totalPages}`;
  
  pdf.text(leftText, 20, pageHeight - 12);
  pdf.text(rightText, pageWidth - 20, pageHeight - 12, { align: 'right' });

  // Website
  pdf.setTextColor(...COLORS.secondary);
  pdf.text('www.altuvera.com', pageWidth / 2, pageHeight - 12, { align: 'center' });
};

