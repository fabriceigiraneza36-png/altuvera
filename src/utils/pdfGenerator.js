/**
 * Download PDF with enhanced error handling
 */
export const downloadPDF = (pdf, filename = 'document.pdf') => {
  try {
    if (!pdf) throw new Error('No PDF document provided');
    pdf.save(filename);
    return { success: true, message: 'PDF downloaded successfully' };
  } catch (error) {
    console.error('PDF Download Error:', error);
    return { success: false, message: error.message };
  }
};

/**
 * Preview PDF in new tab
 */
export const previewPDF = (pdf) => {
  try {
    if (!pdf) throw new Error('No PDF document provided');
    const blob = pdf.output('blob');
    const url = URL.createObjectURL(blob);
    window.open(url, '_blank');
    return { success: true, message: 'PDF opened in new tab' };
  } catch (error) {
    console.error('PDF Preview Error:', error);
    return { success: false, message: error.message };
  }
};